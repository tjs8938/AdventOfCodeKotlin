fun calcScoreP1(n: Long, exp: Long, mod: Long): Long {
    var result = 1L
    val remainders = mutableListOf<Long>()
    while (remainders.size < exp) {
        result = (result * n) % mod
        remainders.add(result)
    }
    return remainders.reversed().joinToString(separator = "") { it.toString() }.toLong()
}

fun calcScoreP2(n: Long, exp: Long, mod: Long): Long {
    val modHelper = ModularArithmetic(mod)
    var result = modHelper.pow(n, exp - 5).toLong()
    val remainders = mutableListOf<Long>()
    while (remainders.size < 5) {
        result = (result * n) % mod
        remainders.add(result)
    }
    return remainders.reversed().joinToString(separator = "") { it.toString() }.toLong()
}

fun calcScoreP3(n: Long, exp: Long, mod: Long): Long {
    var current: Long? = null
    var sum = 0L
    val remainders = mutableListOf<Long>()

    do {
        sum += current ?: 0L

        if (current != null) {
            remainders.add(current)
        }
        current = ((current ?: 1L) * n) % mod
    }  while (current !in remainders)

    val firstHit = remainders.indexOf(current)
    val loopSize = remainders.size - firstHit
    val loops = (exp - firstHit) / loopSize
    val remainder = (exp - firstHit) % loopSize

    sum = remainders.take(firstHit).sum()
    sum += loops * remainders.drop(firstHit).sum()
    sum += remainders.drop(firstHit).take(remainder.toInt()).sum()

    return sum
}

private fun bestScore(input: List<String>, calcScore: (Long, Long, Long) -> Long) {
    val maps = input.map { line ->
        line.split(" ").associate {
            val (key, value) = it.split("=")
            key to value.toLong()
        }
    }.maxOf { params ->
        val a = params["A"]!!
        val b = params["B"]!!
        val c = params["C"]!!
        val x = params["X"]!!
        val y = params["Y"]!!
        val z = params["Z"]!!
        val m = params["M"]!!

        val part1 = calcScore(a, x, m)
        val part2 = calcScore(b, y, m)
        val part3 = calcScore(c, z, m)

        part1 + part2 + part3
    }

    println(maps)
}

